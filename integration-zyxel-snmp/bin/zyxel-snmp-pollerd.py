class IntegrationZyxelSnmp:
    """
    zyxel SNMP poller.

    Architecture:
      1. Fetches devices from OpenSearch (filtered by SNMP credentials).
      2. Probes each device with the core OID to confirm it is pollable.
      3. Probes optional OID groups once per device (caches results).
      4. Polls confirmed devices on a fixed interval using worker threads.
      5. Publishes metrics to Redis ``stream:stats``.
    """

    def __init__(self, args):
        self.running = True
        self.config = rapax.load_config()
        self.logger = self._setup_logging()

        # CLI parameters
        self.poll_interval = args.poll_interval
        self.snmp_timeout = args.snmp_timeout
        self.worker_threads = args.worker_threads
        self.component_id = args.component_id
        self.config_update_interval = args.config_update_interval
        self.hostname = os.uname()[1]

        # Thread-safe structures
        self.poll_queue = queue.Queue()
        self.config_lock = threading.Lock()
        self.capable_lock = threading.Lock()
        self.capabilities_lock = threading.Lock()

        # Device list and caches
        self.devices = {}                  # deviceName -> device dict
        self.capable_cache = {}            # mgmt_ip -> bool
        self.device_capabilities = {}      # deviceName -> { group: [oid_names] }

        signal.signal(signal.SIGTERM, self._handle_signal)
        signal.signal(signal.SIGINT, self._handle_signal)

        self.logger.info("[%s] Initialised (interval=%ds, timeout=%dms, "
                         "workers=%d)", SOURCE, self.poll_interval,
                         self.snmp_timeout, self.worker_threads)

    # ------------------------------------------------------------------
    # Logging
    # ------------------------------------------------------------------

    def _setup_logging(self):
        logger = logging.getLogger(SOURCE)
        logger.setLevel(getattr(
            logging, os.environ.get('LOG_LEVEL', 'INFO').upper(), logging.INFO))
        if not logger.handlers:
            handler = logging.StreamHandler(sys.stdout)
            handler.setFormatter(logging.Formatter(
                '%(asctime)s %(levelname)s %(message)s'))
            logger.addHandler(handler)
        return logger

    def _handle_signal(self, signum, _frame):
        self.logger.info("[%s] Received signal %s, shutting down…",
                         SOURCE, signum)
        self.running = False

    # ------------------------------------------------------------------
    # SNMP helpers
    # ------------------------------------------------------------------

    def snmp_get(self, host, community, oid, timeout_ms):
        """Perform a single SNMP GET.  Returns the value or None."""
        try:
            timeout_s = timeout_ms / 1000.0
            err_ind, err_stat, _err_idx, var_binds = next(
                getCmd(
                    SnmpEngine(),
                    CommunityData(community),
                    UdpTransportTarget((host, 161),
                                      timeout=timeout_s, retries=1),
                    ContextData(),
                    ObjectType(ObjectIdentity(oid)),
                )
            )
            if err_ind or err_stat:
                return None
            for _oid, val in var_binds:
                if 'noSuch' in str(val):
                    return None
                return val
        except Exception:
            return None

    def snmp_bulk_get(self, host, community, oid_list, timeout_ms):
        """GET multiple OIDs in a single request.  Returns {oid: value}."""
        try:
            timeout_s = timeout_ms / 1000.0
            obj_types = [ObjectType(ObjectIdentity(o)) for o in oid_list]
            err_ind, err_stat, _err_idx, var_binds = next(
                getCmd(
                    SnmpEngine(),
                    CommunityData(community),
                    UdpTransportTarget((host, 161),
                                      timeout=timeout_s, retries=1),
                    ContextData(),
                    *obj_types,
                )
            )
            if err_ind or err_stat:
                return {}
            results = {}
            for oid, val in var_binds:
                if 'noSuch' not in str(val):
                    results[str(oid)] = val
            return results
        except Exception:
            return {}

    # ------------------------------------------------------------------
    # Device discovery
    # ------------------------------------------------------------------

    def get_devices_from_opensearch(self):
        """Fetch devices from OpenSearch and probe for SNMP capability."""
        os_cfg = self.config.get('historical', {}).get('opensearch', {})
        url = f"{os_cfg.get('url', 'http://rapax-opensearch:9200')}" \
              f"/devices-*/_search"

        query = {
            "query": {"match_all": {}},
            "sort": [{"lastSeen": {"order": "desc"}}],
            "size": 10000,
            "collapse": {"field": "deviceName.keyword"},
        }

        try:
            import requests
            auth = None
            if os_cfg.get('username'):
                auth = (os_cfg['username'], os_cfg.get('password', ''))
            resp = requests.post(url, json=query, auth=auth,
                                 verify=False, timeout=30)
            resp.raise_for_status()
            hits = resp.json().get('hits', {}).get('hits', [])
        except Exception as exc:
            self.logger.error("[%s] OpenSearch query failed: %s",
                              SOURCE, exc)
            return

        found = 0
        for hit in hits:
            device = hit['_source']
            name = device.get('deviceName', '').strip().strip('"')
            mgmt_ip = device.get('ManagementIpAddress', '').strip()
            if not name or not mgmt_ip:
                continue
            if not self._has_snmp_credentials(device):
                continue

            community = self._get_community_string(device)

            # Check cache first
            with self.capable_lock:
                cached = self.capable_cache.get(mgmt_ip)
            if cached is False:
                continue
            if cached is None:
                is_capable = self._probe_capable(mgmt_ip, community)
                with self.capable_lock:
                    self.capable_cache[mgmt_ip] = is_capable
                if not is_capable:
                    continue

            device['_community'] = community
            with self.config_lock:
                self.devices[name] = device
            found += 1

        self.logger.info("[%s] Device refresh: %d pollable devices "
                         "(from %d hits)", SOURCE, found, len(hits))

    @staticmethod
    def _has_snmp_credentials(device):
        sec = device.get('securityInformation', [])
        if not sec:
            return False
        for cred in sec:
            if cred.get('type') == 'snmp' or 'string' in cred.get('data', {}):
                return True
        return len(sec) > 0

    @staticmethod
    def _get_community_string(device):
        for cred in device.get('securityInformation', []):
            data = cred.get('data', {})
            if 'string' in data:
                return data['string']
        return 'public'

    def _probe_capable(self, mgmt_ip, community):
        """Probe device with core OID to confirm it is pollable."""
        result = self.snmp_get(mgmt_ip, community, PROBE_OID,
                               self.snmp_timeout)
        return result is not None

    # ------------------------------------------------------------------
    # Capability probing (optional OID groups)
    # ------------------------------------------------------------------

    def _probe_capabilities(self, device):
        """Probe which optional OID groups a device supports."""
        name = device.get('deviceName', '')
        mgmt_ip = device.get('ManagementIpAddress', '')
        community = device.get('_community', 'public')

        with self.capabilities_lock:
            if name in self.device_capabilities:
                return self.device_capabilities[name]

        caps = {}
        for group_name, oids in OPTIONAL_OID_GROUPS.items():
            available = []
            for oid_name, oid in oids.items():
                val = self.snmp_get(mgmt_ip, community, oid,
                                    self.snmp_timeout)
                if val is not None:
                    available.append(oid_name)
            caps[group_name] = available

        with self.capabilities_lock:
            self.device_capabilities[name] = caps

        self.logger.info("[%s] Capabilities for %s: %s",
                         SOURCE, name,
                         {g: len(v) for g, v in caps.items()})
        return caps

    # ------------------------------------------------------------------
    # Metric collection
    # ------------------------------------------------------------------

    def collect_core_metrics(self, device, timestamp):
        """Collect core OIDs via bulk GET."""
        name = device.get('deviceName', '')
        mgmt_ip = device.get('ManagementIpAddress', '')
        community = device.get('_community', 'public')

        oid_list = list(CORE_OIDS.values())
        results = self.snmp_bulk_get(mgmt_ip, community, oid_list,
                                     self.snmp_timeout)
        if not results:
            self.logger.debug("[%s] No core metrics for %s", SOURCE, name)
            return

        # Publish each metric
        for oid_name, oid_str in CORE_OIDS.items():
            val = results.get(oid_str)
            if val is None:
                continue
            try:
                numeric_val = float(str(val))
            except (ValueError, TypeError):
                continue
            display_name = METRIC_DISPLAY_NAMES.get(oid_name, oid_name)
            self._publish_metric(timestamp, name, display_name,
                                 numeric_val, device, mgmt_ip)

        # Compute derived metrics (e.g. memory usage %)
        self._compute_derived_metrics(results, timestamp, name, device,
                                      mgmt_ip)

    def collect_optional_metrics(self, device, timestamp, caps):
        """Collect optional OID groups (temperature, fan, etc.)."""
        name = device.get('deviceName', '')
        mgmt_ip = device.get('ManagementIpAddress', '')
        community = device.get('_community', 'public')

        for group_name, available in caps.items():
            group_oids = OPTIONAL_OID_GROUPS.get(group_name, {})
            for oid_name in available:
                oid = group_oids.get(oid_name)
                if not oid:
                    continue
                val = self.snmp_get(mgmt_ip, community, oid,
                                    self.snmp_timeout)
                if val is None:
                    continue
                try:
                    numeric_val = float(str(val))
                except (ValueError, TypeError):
                    continue
                display_name = METRIC_DISPLAY_NAMES.get(oid_name, oid_name)
                self._publish_metric(timestamp, name, display_name,
                                     numeric_val, device, mgmt_ip)

    def _compute_derived_metrics(self, results, timestamp, name, device,
                                 mgmt_ip):
        """Compute derived metrics (e.g. memory usage %) from raw values."""
        try:
            total = int(results.get(CORE_OIDS.get('memTotal', ''), 0))
            free = int(results.get(CORE_OIDS.get('memFree', ''), 0))
            buffers = int(results.get(CORE_OIDS.get('memBuffer', ''), 0))
            cache = int(results.get(CORE_OIDS.get('memCache', ''), 0))
            if total > 0:
                used = total - free - buffers - cache
                pct = (used / total) * 100.0
                self._publish_metric(timestamp, name, 'Memory Usage%',
                                     round(pct, 2), device, mgmt_ip)
        except (ValueError, TypeError, ZeroDivisionError):
            pass

    def _publish_metric(self, timestamp, device_name, metric_name, value,
                        device, mgmt_ip):
        """Publish a single metric to Redis stream:stats."""
        metric = {
            '@timestamp': timestamp,
            'deviceName': device_name,
            'metricName': metric_name,
            'value': float(value),
            'tags': {
                'componentId': self.component_id,
                'managementIP': mgmt_ip,
                'location': device.get('deviceLocation', ''),
                'source': SOURCE,
                'vendor': device.get('vendor', ''),
                'category': device.get('deviceCategory', ''),
            },
        }
        try:
            rapax.send_message(self.logger, 'stats', metric)
        except Exception as exc:
            self.logger.error("[%s] Failed to publish %s for %s: %s",
                              SOURCE, metric_name, device_name, exc)

    # ------------------------------------------------------------------
    # Poll cycle
    # ------------------------------------------------------------------

    def poll_device(self, device, timestamp):
        """Poll a single device for all metrics."""
        caps = self._probe_capabilities(device)
        self.collect_core_metrics(device, timestamp)
        self.collect_optional_metrics(device, timestamp, caps)

    def poll_worker(self):
        """Worker thread: drain the poll queue."""
        while self.running:
            try:
                item = self.poll_queue.get(timeout=1)
            except queue.Empty:
                continue
            if item is None:
                self.poll_queue.task_done()
                break
            try:
                self.poll_device(item['device'], item['timestamp'])
            except Exception as exc:
                self.logger.error("[%s] Worker error for %s: %s",
                                  SOURCE,
                                  item['device'].get('deviceName', '?'),
                                  exc)
            finally:
                self.poll_queue.task_done()

    def get_aligned_timestamp(self, interval):
        """Return an ISO timestamp aligned to the polling interval."""
        now = time.time()
        aligned = (now // interval) * interval
        return datetime.utcfromtimestamp(aligned).strftime(
            '%Y-%m-%dT%H:%M:%S.%fZ')

    def initiate_poll_cycle(self):
        """Queue all devices for polling. Returns (timestamp, count)."""
        ts = self.get_aligned_timestamp(self.poll_interval)
        with self.config_lock:
            devices = list(self.devices.values())
        if not devices:
            self.logger.warning("[%s] No devices to poll", SOURCE)
            return ts, 0
        for d in devices:
            self.poll_queue.put({'device': d, 'timestamp': ts})
        self.logger.info("[%s] Poll cycle queued: %d devices at %s",
                         SOURCE, len(devices), ts)
        return ts, len(devices)

    def monitor_poll_progress(self, start_time, timestamp, device_count):
        """Watch the queue; abort if poll exceeds the interval."""
        while self.running:
            remaining = self.poll_queue.qsize()
            elapsed = time.time() - start_time
            if remaining == 0:
                self.logger.info("[%s] Poll completed in %.2fs",
                                 SOURCE, elapsed)
                self._publish_metric(
                    timestamp, self.hostname,
                    f'{SOURCE} Poll Duration (s)',
                    round(elapsed, 2),
                    {'deviceLocation': '', 'vendor': '',
                     'deviceCategory': ''},
                    self.hostname,
                )
                break
            if elapsed > self.poll_interval:
                self.logger.error(
                    "[%s] Poll overrun (%.1fs > %ds), %d items remain",
                    SOURCE, elapsed, self.poll_interval, remaining)
                self.running = False
                os._exit(1)
            time.sleep(1)

    # ------------------------------------------------------------------
    # Main loop
    # ------------------------------------------------------------------

    def run(self):
        """Start workers and enter the main poll loop."""
        # Initial device discovery
        self.get_devices_from_opensearch()

        workers = []
        for i in range(self.worker_threads):
            t = threading.Thread(target=self.poll_worker,
                                 name=f'{SOURCE}-worker-{i+1}',
                                 daemon=True)
            t.start()
            workers.append(t)

        last_config = time.time()

        while self.running:
            try:
                now = time.time()
                # Periodically refresh device list
                if now - last_config >= self.config_update_interval:
                    self.get_devices_from_opensearch()
                    last_config = now

                # Sleep until next aligned interval
                next_poll = ((now // self.poll_interval) + 1) * \
                    self.poll_interval
                sleep_for = next_poll - now
                if sleep_for > 0:
                    time.sleep(sleep_for)

                # Start poll cycle
                poll_start = time.time()
                ts, count = self.initiate_poll_cycle()
                if count > 0:
                    mon = threading.Thread(
                        target=self.monitor_poll_progress,
                        args=(poll_start, ts, count),
                        name='PollMonitor', daemon=True)
                    mon.start()

            except KeyboardInterrupt:
                self.running = False
                break
            except Exception as exc:
                self.logger.error("[%s] Main loop error: %s", SOURCE, exc)
                time.sleep(5)

        # Graceful shutdown
        self.logger.info("[%s] Shutting down workers…", SOURCE)
        for _ in workers:
            self.poll_queue.put(None)
        for w in workers:
            w.join(timeout=5)
        self.logger.info("[%s] Stopped", SOURCE)


